
package com.mohit;


public class UserData 
{
    String name,email,gender,address;

    public UserData(String name, String email, String gender, String address)
    {
        this.name = name;
        this.email = email;
        this.gender = gender;
        this.address = address;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getGender() {
        return gender;
    }

    public String getAddress() {
        return address;
    }

    
    
    
}
